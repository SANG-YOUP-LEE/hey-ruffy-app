   1) 다짐의 생성·수정·삭제 및 반복 주기 설정
   2) 다짐 타이머의 실행과 알림
   3) 다짐 실행 정보의 저장 및 진행 현황 시각화(산책 모드 포함)
   4) 달성 횟수에 따른 보상 카드 발행 및 기록
   5) 다짐 관련 콘텐츠 작성 및 제공
5. 회사는 특정 서비스를 유료로 제공할 수 있으며, 체험 목적의 무료 이용기간을 제공할 수 있습니다.
6. 회사는 연중무휴 1일 24시간 제공을 원칙으로 합니다.
7. 별도 동의를 받은 경우 서비스 이용과 관련한 유용한 정보를 팝업 메시지, 푸시 알림 등으로 제공할 수 있습니다.

제13조 [서비스의 변경 및 중지]
1. 회사는 운영상·기술상 필요에 따라 서비스를 변경할 수 있으며 변경 전에 서비스 내에 공지합니다. 버그 수정이나 긴급 업데이트 등 부득이한 경우 또는 중대한 변경에 해당하지 않는 경우에는 사후 공지할 수 있습니다.
2. 영업양도·분할·합병 등에 따른 영업 폐지, 현저한 수익 악화 등 중대한 사유로 서비스 지속이 어려운 경우 서비스 전부를 중단할 수 있습니다. 이 경우 중단일자 30일 이전까지 중단 일자, 사유, 보상 조건 등을 공지하고 제5조에 따라 통지합니다.
3. 다음 각 호에 해당하는 경우 서비스의 전부 또는 일부를 제한하거나 중지할 수 있습니다.
   1) 전시, 사변, 천재지변, 국가비상사태 등 불가항력
   2) 정전, 설비 장애, 이용량 폭주 등 정상적 이용 지장
   3) 설비 보수 등 공사로 인한 부득이한 경우
   4) 회사의 제반 사정으로 서비스를 할 수 없는 경우

제14조 [서비스의 이용 제한]
이용자가 제9조를 위반하는 경우 회사는 사전 통보 없이 서비스 이용을 제한할 수 있습니다.

제15조 [정보의 수집 등]
1. 회사는 이용자의 이메일 주소, 결제 관련 정보 등의 개인정보와 서비스 이용 내역 등을 수집·이용할 수 있으며, 구체적 수집 항목·이용 목적 등은 개인정보 처리방침에 따릅니다.
2. 회사는 서비스의 원활·안정적 운영 및 품질 개선을 위하여 개인정보를 제외한 디바이스 정보(설정, 사양, OS, 버전 등)를 수집·활용할 수 있습니다.
3. 회사는 서비스 개선 및 이용자 대상 서비스 소개 등을 위한 목적으로 이용자에게 추가정보를 요청할 수 있으며, 이용자는 승낙 또는 거절할 수 있습니다. 회사는 요청 시 거절 가능함을 함께 고지합니다.

제16조 [유료서비스의 이용]
1. 본 약관에 동의한 이용자가 유료서비스 이용을 신청하고 회사가 이를 승낙함(구매·결제 완료 표시 시점)으로써 유료서비스 이용계약이 성립합니다.
2. 회사는 결제 전에 유료서비스의 내용, 가격, 이용기간, 이용방법, 구독취소 및 정기구독 해지 조건과 방법, 환불 사항 등을 구매 페이지를 통해 제공합니다.
3. 이용자는 신청 시 사실과 일치하는 정보를 기재하고, 선택한 결제수단에 필요한 정보를 정확히 제공해야 합니다. 변경 시 지체 없이 회사가 정한 절차에 따라 고지해야 합니다.
4. 이용자는 회사가 지정하는 결제수단으로 유료서비스를 이용할 수 있으며, 프로모션 또는 제3자로부터 받은 선물권을 이용할 수 있습니다.

제17조 [유료서비스의 변경]
1. 정기구독 유료서비스에 한하여 회사가 정한 정책에 따라 변경 신청을 할 수 있습니다.
2. 변경에 따라 정기구독 서비스는 즉시 교체되며, 기존 결제 서비스의 잔여 이용기간은 보전되지 않습니다. 회사는 변경 완료 전 해당 사실을 사전 공지하며, 공지 후의 변경 신청은 이에 동의한 것으로 간주합니다.

제18조 [유료서비스의 해지 및 환불]
1. 정기구독 방식의 유료서비스는 앱 내 관리 페이지, 전자우편 문의, 스토어 관리 페이지 등을 통해 해지할 수 있습니다. 해지 시 다음 이용기간부터 자동 결제가 이루어지지 않습니다. 이미 결제된 기간 동안의 서비스는 정상 이용할 수 있습니다.
2. 이용자는 언제든지 정기구독 유료서비스의 취소 및 해지를 신청할 수 있으며, 회사는 관계 법령에 따라 신속히 처리합니다.
3. 이용자는 유료서비스 종류에 따라 다음의 기한 내에 해지 및 환불을 요청하는 경우 결제금액의 100% 환불을 받을 수 있습니다. 단, 무료 체험 기간 내에는 실제 결제가 이루어지지 않아 환불이 성립되지 않습니다.
   1) 1개월 정기구독: 정기 결제일로부터 7일 이내
   2) 6개월 정기구독: 정기 결제일로부터 30일 이내
   3) 12개월 정기구독: 정기 결제일로부터 30일 이내
   4) 1개월 선물권: 구매일로부터 7일 이내
   5) 6개월 선물권: 구매일로부터 30일 이내
   6) 12개월 선물권: 구매일로부터 30일 이내
4. 회사는 정당한 해지 또는 환불 신청을 받은 경우 각 유료서비스에 명시된 안내에 따라 절차를 이행합니다. 단, 다음 각 호에 해당하는 경우 해지 및 환불을 이행하지 않을 수 있습니다.
   1) 이용자의 실수로 이용기간 내 서비스를 이용하지 못한 경우
   2) 어떠한 통보 없이 앱 삭제 후 이용하지 않는 경우
   3) 환불받고자 하는 선물권이 이미 유료서비스 이용에 적용된 경우
   4) 환불 요청 선물권의 구매자가 이용자 본인이 아닌 경우
   5) 기타 정당한 해지 및 환불 사유로 판명되지 않은 경우
5. 이용자가 제9조의 의무를 위반한 경우 회사는 이용계약을 해지할 수 있으며 환불 의무를 부담하지 않습니다.

제19조 [저작권 등의 귀속]
1. 회사가 작성·제공하는 저작물에 대한 저작권 및 기타 지식재산권은 회사에 귀속합니다.
2. 회원이 작성한 게시물의 저작권은 회원에게 귀속되며, 해당 게시물이 타인의 지식재산권을 침해하여 발생하는 모든 책임은 회원에게 있습니다.
3. 회사가 게시된 회원 게시물을 수정·편집하거나 2차 저작물을 만들어 이용하는 경우 사전에 회원의 이용 허락을 받아야 합니다.
4. 이용자는 회사에 지식재산권이 귀속된 정보를 사전 승인 없이 복제, 전송, 출판, 배포, 편집 등으로 영리목적으로 이용하거나 제3자에게 이용하게 하여서는 안 됩니다.
5. 이용자의 게시물이 저작권법 등 관련 법령에 위반되거나 사생활 침해·명예훼손 등 타인의 권리를 침해한다고 인정되는 경우 회사는 관련법이 정한 절차에 따라 게시 중단 및 삭제 등을 요청할 수 있습니다. 이로 인한 민·형사상 책임은 해당 이용자가 부담합니다.
6. 권리자의 요청이 없는 경우라도 권리침해가 인정될 만한 사유가 있거나 회사 정책·관련법 위반 시 회사는 관련법에 따라 해당 게시물에 대한 접근을 임시 차단하거나 삭제할 수 있습니다.
7. 이용자가 게시물을 서비스에 게재하는 것은 다른 이용자가 서비스 내에서 해당 게시물을 사용하거나 회사가 검색 결과로 사용하는 것을 허락한 것으로 봅니다. 이용자는 공개 범위를 관리 기능으로 조정할 수 있습니다.

제20조 [광고의 제공]
1. 회사는 서비스를 유지하기 위하여 서비스 내에 광고를 게재할 수 있으며, 이용자는 서비스 이용 시 노출되는 광고 게재에 동의합니다.
2. 회사가 제공하는 제3자 주체의 광고에 이용자가 참여·교신·거래함으로써 발생하는 손실과 손해에 대해서 회사는 책임을 부담하지 않습니다.
3. 회사는 서비스 개선 및 이용자 대상 서비스 소개 등을 위한 목적으로 이용자에게 추가 정보를 요구할 수 있으며, 이용자는 승낙 또는 거절할 수 있습니다.
4. 이용자의 사전 동의가 있는 경우 회사는 수집한 개인정보를 활용하여 광고 및 정보를 푸시 알림, 전자우편 등으로 제공할 수 있으며, 이용자는 언제든지 수신을 거부할 수 있습니다.

제21조 [손해배상]
1. 회사 또는 이용자는 본 약관을 위반하여 상대방에게 손해를 입힌 경우 그 손해를 배상할 책임이 있습니다. 다만 고의 또는 과실이 없는 경우에는 그러하지 않습니다.
2. 회사가 개별서비스 제공자와 제휴 계약을 맺고 개별서비스를 제공하는 경우 회원이 개별서비스 이용약관에 동의한 뒤 개별서비스 제공자의 고의 또는 과실로 손해가 발생한 때에는 그 손해에 대해서 개별서비스 제공자가 책임을 집니다.

제22조 [회사의 면책]
1. 천재지변 또는 이에 준하는 불가항력으로 인하여 서비스를 제공할 수 없는 경우 회사는 책임을 지지 않습니다.
2. 설비 보수·교체·정기점검·공사 등으로 발생한 손해에 대하여 회사는 책임을 지지 않습니다. 다만 회사의 고의 또는 과실이 있는 경우는 제외합니다.
3. 이용자의 고의 또는 과실로 인한 이용 장애에 대하여 회사는 책임을 지지 않습니다. 다만 이용자에게 부득이하거나 정당한 사유가 있는 경우는 제외합니다.
4. 이용자가 게시한 정보·자료 등의 신뢰성·정확성에 대하여 회사는 고의 또는 중대한 과실이 없는 한 책임을 지지 않습니다.
5. 이용자 간 또는 제3자와의 분쟁에 대하여 회사는 개입 의무가 없으며, 이로 인한 손해에 책임을 지지 않습니다.
6. 무료로 제공되는 서비스 이용과 관련하여 발생한 손해에 대하여 회사는 책임을 지지합니다. 다만 회사의 고의 또는 중과실이 있는 경우는 제외합니다.
7. 이용자가 기대하는 이익의 상실에 대하여 회사는 책임을 지지 않습니다.
8. 이용자가 모바일 기기 비밀번호, 스토어 비밀번호 등을 관리하지 않아 발생하는 제3자 결제에 대하여 회사는 책임을 지지 않습니다. 다만 회사의 고의 또는 과실이 있는 경우는 제외합니다.
9. 모바일 기기 변경, 번호 변경, 운영체제 변경, 해외 로밍, 통신사 변경 등으로 콘텐츠 기능을 이용할 수 없는 경우 회사는 책임을 지지 않습니다. 다만 회사의 고의 또는 과실이 있는 경우는 제외합니다.
10. 이용자가 회사가 제공하는 콘텐츠나 계정정보를 삭제한 경우 회사는 책임을 지지 않습니다. 다만 회사의 고의 또는 과실이 있는 경우는 제외합니다.

제23조 [회원의 고충처리 및 분쟁해결]
1. 회사는 이용자의 편의를 고려하여 의견·불만 제시 방법을 서비스 내 또는 그 연결화면에 안내합니다.
2. 정당한 의견·불만으로 객관적으로 인정되는 경우 회사는 합리적인 기간 내 신속하게 처리합니다. 장기간이 소요되는 경우 사유와 처리 일정을 공지하거나 제5조에 따라 통지합니다.

제24조 [재판권 및 준거법]
이 약관은 대한민국 법률에 따라 규율·해석됩니다. 회사와 이용자 간 분쟁으로 소송이 제기되는 경우 법령에 정한 절차에 따른 법원을 관할 법원으로 합니다.

제25조 [약관의 해석 및 개인정보 처리방침과의 관계] (신설)
본 약관은 회사의 개인정보 처리방침과 함께 적용됩니다. 두 문서 간 내용이 상충하는 경우 개인정보 보호 수준이 더 높은 방향으로 해석되며, 이용자는 언제든지 개인정보 관련 권리를 행사할 수 있습니다.

제26조 [마케팅 콘텐츠 활용] (신설)
1. 회사는 사용자에게 유익한 정보를 제공하고 서비스 품질을 개선하기 위해 마케팅, 프로모션 및 리서치 목적의 콘텐츠를 운영할 수 있습니다.
2. 회사는 이용자가 자발적으로 작성·등록한 콘텐츠(예: 다짐명, 후기, 피드백 등) 중 개인 식별이 불가능한 형태의 정보에 한하여 마케팅 목적 활용 또는 외부 노출(예: 홈페이지, SNS, 앱스토어 등)을 진행할 수 있습니다. 이용자는 앱 설정 또는 이메일 등을 통해 언제든지 해당 활용을 철회할 수 있습니다.
